                <h3>¿Quieres hacer el test de ansiedad y depresión?</h3>
                <p>
                    El test de ansiedad y depresión es una herramienta diagnóstica útil que ayuda a identificar los signos y 
                    síntomas de estas condiciones de salud mental. Este examen se compone generalmente de una
                    serie de preguntas estandarizadas que exploran tus emociones, pensamientos y comportamientos
                    en las últimas semanas. Los ítems pueden indagar en aspectos como cambios en los patrones de
                    sueño y apetito, niveles de energía, dificultad para concentrarse, sentimientos de desesperanza,
                    miedo o preocupación excesiva, así como pensamientos de muerte o suicidio. Al final, las respuestas
                    se califican de acuerdo a una escala, proporcionando una medida de la severidad de los síntomas
                    de ansiedad y/o depresión. Es importante recordar que estos test son una herramienta de screening y
                    no un diagnóstico definitivo. Si los resultados indican la posible presencia de ansiedad o depresión, 
                    se recomienda buscar la ayuda de un profesional de salud mental para una evaluación más completa.
                 </p>

